#include <iostream>

using namespace std;

int main(){
    // we have 3 parameter in for loop
    // initial statement, condition, post statement
    // - initial statement executed once before the for loop
    // - condition is checked in every for loop iteration
    // - post statement executed every loop iteration
    for(int index = 1; index < 6; index++){
        cout << index << endl;
    }

    return 0;
}